/**
 * Get color key from RGB array
 * @param {number[]} rgb - RGB color array [r, g, b]
 * @returns {string} Color key "r,g,b"
 */
export function getColorKey(rgb) {
  return rgb.join(',');
}

/**
 * Get RGB color from CSS string
 * @param {string} cssColor - CSS color string like "rgb(255, 255, 255)"
 * @returns {number[]} RGB array [r, g, b]
 */
export function getRGBFromCSS(cssColor) {
  const match = cssColor.match(/rgb\((\d+),\s*(\d+),\s*(\d+)\)/);
  if (match) {
    return [parseInt(match[1]), parseInt(match[2]), parseInt(match[3])];
  }
  return [0, 0, 0];
}

/**
 * Get CSS color string from RGB array
 * @param {number[]} rgb - RGB array [r, g, b]
 * @returns {string} CSS color string "rgb(r, g, b)"
 */
export function getRGBCSS(rgb) {
  return `rgb(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`;
}

/**
 * Find color info by RGB value
 * @param {number[]} rgb - RGB array [r, g, b]
 * @returns {object|null} Color info object or null if not found
 */
export function findColorByRGB(rgb) {
  const key = getColorKey(rgb);
  return colorPalette.find(c => getColorKey(c.rgb) === key) || null;
}
